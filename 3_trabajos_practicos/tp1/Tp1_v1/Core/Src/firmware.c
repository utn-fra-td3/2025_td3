#include "main.h"
#include "cmsis_os.h"

osThreadId_t defaultTaskHandle;
const osThreadAttr_t defaultTask_attributes = {
  .name = "defaultTask",
  .stack_size = 128 * 4,
  .priority = (osPriority_t) osPriorityNormal,
};

void led_off(void *p){
	for(;;){
		HAL_GPIO_WritePin(LD3_GPIO_Port, LD3_Pin, GPIO_PIN_RESET);
		vTaskDelay(500/portTICK_RATE_MS);
	}
}
void led_on(void *p){
	for(;;){
		HAL_GPIO_WritePin(LD3_GPIO_Port, LD3_Pin, GPIO_PIN_SET);
		vTaskDelay(1000/portTICK_RATE_MS);
		}
}
/*
 * Primero se apaga el led, inmediatamente se prende y se espera 500ms
 * Pasados los 500ms se apaga el led, y se sigue esperando a que terminen los 1000ms de la tarea LED ON
 * Cuando pasan dichos 1000ms, se prende el led. Logrando un tiempo de prendido/apagado de 500ms
 *
 */

void SystemClock_Config(void);
static void MX_GPIO_Init(void);
void StartDefaultTask(void *argument);

int main(void)
{
  HAL_Init();					//Inicializo API HAL
  SystemClock_Config();			//Configuro clock del sistema
  MX_GPIO_Init();				//Inicializo GPIOs
  osKernelInitialize();			//Inicio el kernel de FreeRtos (no inicio el scheduler)

  defaultTaskHandle = osThreadNew(StartDefaultTask, NULL, &defaultTask_attributes);	//Creo task idle

  if(xTaskCreate(led_on,"Led ON", configMINIMAL_STACK_SIZE, NULL, osPriorityNormal, NULL)!=pdPASS)
	  Error_Handler();
  if(xTaskCreate(led_off,"Led OFF", configMINIMAL_STACK_SIZE, NULL, osPriorityNormal+1, NULL)!=pdPASS)
	  Error_Handler();		//Me aseguro que la tarea Led OFF inicie al darle mayor prioridad

  osKernelStart();				//Inicializo el scheduler

  while (1)
  {
  }
}

